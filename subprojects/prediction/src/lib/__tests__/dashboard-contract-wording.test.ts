import { readFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { describe, expect, it } from 'vitest'

describe('dashboard contract wording', () => {
  it('anchors preflight, approval, research, and live governance wording for the dashboard', () => {
    const root = resolve(process.cwd())
    const contract = readFileSync(resolve(root, 'docs/dashboard-contract.md'), 'utf8')
    const readme = readFileSync(resolve(root, 'README.md'), 'utf8')
    const guide = readFileSync(resolve(root, 'docs/cli-agent-control.md'), 'utf8')

    expect(contract).toContain('preflight-first')
    expect(contract).toContain('proof chain')
    expect(contract).toContain('advisor-first')
    expect(contract).toContain('kill criteria')
    expect(contract).toContain('double approval')
    expect(contract).toContain('SSE/events')
    expect(contract).toContain('governed venue execution')
    expect(contract).toContain('approval_ticket')
    expect(contract).toContain('operator_thesis')
    expect(contract).toContain('research_pipeline_trace')
    expect(contract).toContain('optional companion fields')
    expect(contract).toContain('source of truth')

    expect(readme).toContain('preflight-first')
    expect(readme).toContain('proof chain')
    expect(readme).toContain('advisor-first')
    expect(readme).toContain('kill criteria')
    expect(readme).toContain('SSE')
    expect(readme).toContain('execution_mode=live')
    expect(readme).toContain('approval_ticket')
    expect(readme).toContain('operator_thesis')
    expect(readme).toContain('research_pipeline_trace')
    expect(readme).toContain('metadonnees optionnelles')
    expect(readme).toContain('flux canonique de live intent / trade intent guard')

    expect(guide).toContain('preflight-first')
    expect(guide).toContain('proof chain')
    expect(guide).toContain('advisor-first')
    expect(guide).toContain('kill criteria')
    expect(guide).toContain('SSE')
    expect(guide).toContain('events')
    expect(guide).toContain('configured Polymarket transport')
    expect(guide).toContain('approval_ticket')
    expect(guide).toContain('operator_thesis')
    expect(guide).toContain('research_pipeline_trace')
    expect(guide).toContain('read-only governance/research context')
    expect(guide).toContain('source of truth')
    expect(guide).toContain('without treating the absence as an error')
  })
})
